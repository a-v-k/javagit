package com.logisima.javagit.cli.init;

import com.logisima.javagit.JavaGitException;
import com.logisima.javagit.cli.Parser;

/**
 * Parser class to parse the output generated by git init and return a <code>GitInitResponse</code> object.
 */
public class GitInitParser extends Parser {

    private GitInitResponse response;

    /**
     * Constructor.
     */
    public GitInitParser() {
        super();
        response = new GitInitResponse();
    }

    /**
     * Parses a line at a time from the commandline execution output of git init
     */
    public void parseLine(String line) {
        if (line.length() == 0) {
            return;
        }
        // initialized
        if (line.contains("Initialized")) {
            response.setInitialized(true);
        }
        // reinitialized
        else if (line.contains("Reinitialized") || line.contains("Initialized")) {
            response.setReinitialized(true);
        }
    }

    @Override
    public GitInitResponse getResponse() throws JavaGitException {
        return response;
    }
}
